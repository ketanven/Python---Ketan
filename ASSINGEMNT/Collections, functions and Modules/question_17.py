#Write a Python program to check whether an element exists within a
#tuple.

# Tuple to check
mytuple = (1, 2, 3, 4, 5)

# Element to search for
element = 3

# Check if the element exists in the tuple
if element in mytuple:
    print()
    print(f"The element {element} exists in the tuple.")
    print()
else:
    print()
    print(f"The element {element} does not exist in the tuple.")
    print()
