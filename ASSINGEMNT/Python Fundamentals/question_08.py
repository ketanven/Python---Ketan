# Write a Python program to test whether a passed letter is a vowel or
# not.

# Take input from the user
letter = input("**ENTER A LETTER TO CHECK IF IT IS VOWEL OR NOT = ")
print()

# Check if the letter is a vowel
if letter in "AEIOUaeiou":
    print("**ENTERED LETTER IS A VOWEL**")
    print()
else:
    print("**ENTERED LETTER IS NOT A VOWEL**")
    print()
